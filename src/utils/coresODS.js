/**
 * Retorna as cores específicas para um ODS
 * @param {string} codigoODS - Código do ODS (ex: 'ods1', 'ods12')
 * @returns {Object} Objeto contendo as cores para o ODS especificado
 */
export function obterCoresODS(codigoODS) {
  if (coresODS[codigoODS]) {
    return coresODS[codigoODS];
  }
  
  // Retorna cores padrão caso o código não seja encontrado
  console.warn(`Cores não encontradas para o ODS ${codigoODS}. Usando cores padrão.`);
  return {
    primaria: '#1E386A',
    secundaria: 'rgba(30, 56, 106, 0.7)',
    terciaria: 'rgba(30, 56, 106, 0.3)',
    sombra: 'rgba(30, 56, 106, 0.15)'
  };
}